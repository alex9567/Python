#利用urllib读取JSON，然后将JSON解析为Python对象：
from urllib import request
import json
def fetch_data(url):
    req=request.Request(url)#定义一个请求
    req.add_header('User-Agent','Mozilla/6.0 (iPhone; CPU iPhone OS 8_0 like Mac OS X) AppleWebKit/536.26 (KHTML, like Gecko) Version/8.0 Mobile/10A5376e Safari/8536.25')#添加浏览器User-Agent
    with request.urlopen(req)as f:#打开网页  
        return json.loads(f.read().decode('utf-8'))#序列化成Json格式
